package com.djax.ffmpeg.wrapper.extension;

public class MetricExtension 
{	
	private String type;
    private String FeEventId;
    private String AdEventId;
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getFeEventId() {
		return FeEventId;
	}
	public void setFeEventId(String feEventId) {
		FeEventId = feEventId;
	}
	public String getAdEventId() {
		return AdEventId;
	}
	public void setAdEventId(String adEventId) {
		AdEventId = adEventId;
	}
    
    
    
	
}
